import {ActivatedRoute, RouterModule} from '@angular/router';
import {trigger, state, animate, style, transition} from '@angular/animations';
import {AnimationTriggerMetadata} from '@angular/animations';

export function routerTransition() {
  return fadeIn();
}

export function slide() {
  var t:AnimationTriggerMetadata =  trigger('routerTransition', [
    state('void', style({position:'fixed', width:'100%'}) ),
    state('*', style({position:'fixed', width:'100%'}) ),
    transition(':enter', [
      style({transform: 'translateX(-100%)'}),
      animate('0.5s ease-in-out', style({transform: 'translateX(0%)'}))
    ]),
    transition(':leave', [
      style({transform: 'translateX(0%)'}),
      animate('0.5s ease-in-out', style({transform: 'translateX(100%)'}))
    ])
  ]);
  return t;
}

function fadeIn() {
  return trigger(
    'routerTransition',
    [
      state('void', style({opacity:1, width:'100%'}) ),
      state('*', style({opacity:1, width:'100%'}) ),

      transition(":enter", [
        style({ position:'absolute', opacity: 0, transform: 'translateY(-1%)' }),
        animate('0.25s ease-out', style({ position:'relative', opacity: 1, transform: 'translateY(0%)' }))
      ]),
      transition(":leave", [
        style({ position:'absolute', opacity: 0, transform: 'translateY(-1%)' }),
        animate('0.25s ease-out', style({ position:'relative', opacity: 0, transform: 'translateY(0%)' }))
      ])
    ]);
}

